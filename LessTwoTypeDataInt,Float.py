# ЧИСЛА ( int, float):
a = 5  # целое число
b = 3.15  # число с плавающей точкой

# ОПЕРАЦИИ НАД ЧИСЛАМИ

# сложение чисел +
number_one = 12345  # целое число int
number_two = 12346  # целое число int
print(number_one + number_two)  # сложение +

number_one = 3.15  # число с плавающей точкой float
number_two = 3.44  # число с плавающей точкой float
print(number_one + number_two)  # сложение +

# вычитание чисел -
number_one = 434  # целое число int
number_two = 431  # целое число int
print(number_one - number_two)  # вычитание -

number_one = 435.1  # число с плавающей точкой float
number_two = 42.2  # число с плавающей точкой float
print(number_one - number_two)  # вычитание -

# деление чисел /
number_one = 1000  # целое число int
number_two = 500  # целое число int
print(number_one / number_two)  # деление /

number_one = 155.25  # число с плавающей точкой float
number_two = 233  # целое число int
print(number_one / number_two)  # деление /

number_one = 1555
number_two = 25
print(number_one // number_two)  # целочисленное деление /

# умножение чисел *

number_one = 45  # целое число int
number_two = 567  # целое число int
print(number_one * number_two)  # деление *


# возведение в степень **
number_one = 5  # целое число int
number_two = 2.5  # число с плавающей точкой float
print(number_one ** number_two)  # возведение в степень

